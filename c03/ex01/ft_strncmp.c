int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned char	s3;
	unsigned char	s4;
	unsigned int	i;

	i = 0;
	while ((s1[i] != '\0' && i < n) || (s2[i] != '\0' && i < n))
	{
		s3 = s1[i];
		s4 = s2[i];
		if (s3 != s4)
			return (s3 - s4);
		i++;
	}
	return (0);
}
